package com.inspiracode.praxma.rhi.web.monitor.dto;

public class SaleSummary {
	private String agente;
	private double venta;
	private double faltante;

	public String getAgente() {
		return agente;
	}

	public void setAgente(String value) {
		agente = value;
	}

	public double getVenta() {
		return venta;
	}

	public void setVenta(double value) {
		venta = value;
	}

	public double getFaltante() {
		return faltante;
	}

	public void setFaltante(double value) {
		faltante = value;
	}

}
